int	my_ft_lettercount(char const *s, char c)
{
	int len;
	int i;

	len = 0;
	i = 0;
	while (*s)
	{
		if (*s == c)
			i++;
		else
		{
			len++;
			i++;
		}
		s++;
	}
	return (len);
}

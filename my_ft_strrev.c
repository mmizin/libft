void	my_ft_strrev(char *c)
{
	unsigned int	i;
	unsigned int	j;
	char			ret;

	i = 0;
	j = 0;
	while (c[i])
		i++;
	while (j < i)
	{
		ret = c[i - 1];
		c[i - 1] = c[j];
		c[j] = ret;
		i--;
		j++;
	}
}

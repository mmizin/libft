int	my_ft_wordcount(const char *str, char c)
{
	int		i;
	char	*s;

	i = 0;
	s = (char *)str;
	while (*s != '\0')
	{
		if (*s != c && *s != '\0')
		{
			while (*s != c && *s != '\0')
				s++;
			i++;
		}
		else
			s++;
	}
	return (i);
}
